    public Integer queryEmpleado (){
        ConexionBD Objmsql = new ConexionBD ();
        sentence = "SELECT MAX(id_empleado) FROM datos_empleado";
        System.out.println("max" + sentence);
        try{
            resc = Objmsql.consultarAlgo(sentence);
            while (resc.next()){
                ultimo = resc.getInt("MAX(id_empleado)");
            }
        }catch (SQLException ex){
            java.util.logging.Logger.getLogger(JDEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Existen "+ ultimo + "Empleados");
        return ultimo;
    }
    
    
