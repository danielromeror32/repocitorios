    private void jLabel16MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel16MouseClicked
        // Button siguiente. Se mandan los
        if (tf_idempleado.getText().equals("") || tf_nombre.getText().equals("")||
            tf_fechaIngreso.getText().equals("") ||
            tf_curp.getText().equals("") || tf_rfc.getText().equals("")||
            tf_contrato.getText().equals("") || tf_tp.getText().equals("") ||
            tf_categoria.getText().equals("") || tf_tipoPuesto.getText().equals("") ||
            tf_centroCostos.getText().equals("") || tf_digitalRfc.getText().equals("") ||
            tf_digitalCurp.getText().equals("") || tf_digitalIde.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese todos los datos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
            // metodo setdatosempleado para mandar valores al objeto empleado2
            objetoEmpleado.setDatosEmpleado(tf_idempleado.getText(), tf_nombre.getText(), tf_fechaIngreso.getText(),
                tf_curp.getText(), tf_rfc.getText(), tf_contrato.getText(), tf_tp.getText(), tf_categoria.getText(),
                tf_tipoPuesto.getText(), tf_centroCostos.getText(), tf_digitalRfc.getText(), tf_digitalCurp.getText(),
                tf_digitalIde.getText());

            JDEmpleado2 docentes2 = new JDEmpleado2 (objetoEmpleado);
            this.setVisible(false);
            docentes2.setVisible(true);
        }
    }//GEN-LAST:event_jLabel16MouseClicked

